import static android.support.test.espresso.Espresso.onView;
import static android.support.test.espresso.action.ViewActions.click;
import static android.support.test.espresso.assertion.ViewAssertions.matches;
import static android.support.test.espresso.matcher.ViewMatchers.withId;
import static android.support.test.espresso.matcher.ViewMatchers.withText;

@RunWith(AndroidJUnit4.class)
public class Test_17346_1734605 {

    @Rule
    public ActivityTestRule<MainActivity> activityRule = new ActivityTestRule<>(MainActivity.class);

    @Rule
    public Timeout timeout = new Timeout(5, java.util.concurrent.TimeUnit.SECONDS);

    @Test
    public void testUserCannotSubmitWithoutSelectingAPhotoOrVideo() throws InterruptedException {
        // Click on the 'Submit' button without selecting a photo or video
        onView(withId(R.id.id_station_logo)).perform(click());
        Thread.sleep(500); // Wait for 500ms to ensure the action is processed

        // Check if an error message is displayed indicating that a photo or video is required
        onView(withText("Please select a photo or video")).check(matches(isDisplayed()));
    }
}